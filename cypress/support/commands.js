Cypress.Commands.add("logCurrentPage", () => {
  cy.get('[data-cy="pagination-text"]')
    .invoke("text")
    .then((text) => {
      cy.log(`Current page: ${text}`);
    });
});

Cypress.on("fail", (error, runnable) => {
  cy.screenshot(); // take screenshot on failure
  throw error; // throw the error to fail the test
});
